MetaballsGenerator::MetaballsGenerator(
        const box3f& bounds,
        const MetaballSpheres& metaballs,
        brayns::TrianglesCollection& triangles,
        const size_t gridGranularity,
        const float threshold )
{
    CubeGridVertex* vertices;
    CubeGridCube* cubes;

    float scale(1.5);
    size_t numMetaballs = metaballs.size();
    vec3f center = (bounds.lower + bounds.upper) / 2;

    box3f box;
    box.lower = center - scale*bounds.size()/2;
    box.upper = center + scale*bounds.size()/2;

    // initialise the metaBall_ size and positions
    size_t numVertices =
            (gridGranularity+1)*(gridGranularity+1)*(gridGranularity+1);
    vertices = new CubeGridVertex[numVertices];
    size_t numCubes = gridGranularity*gridGranularity*gridGranularity;
    cubes = new CubeGridCube[numCubes];

    size_t x=0;
#pragma omp parallel for
    for(x=0; x<gridGranularity+1; ++x)
    {
        for(size_t y=0; y<gridGranularity+1; ++y)
        {
            for(size_t z=0; z<gridGranularity+1; ++z)
            {
                size_t index =
                        x*(gridGranularity+1)*(gridGranularity+1) +
                        y*(gridGranularity+1) + z;
                CubeGridVertex cgv;
                const vec3f boundSize = bounds.size();
                cgv.position.x =
                        x*boundSize.x/gridGranularity - boundSize.x/2.f;
                cgv.position.y =
                        y*boundSize.y/gridGranularity - boundSize.y/2.f;
                cgv.position.z =
                        z*boundSize.z/gridGranularity - boundSize.z/2.f;

                cgv.position = cgv.position + center;
                cgv.textCoords.x =
                        static_cast<float>(x)/
                        static_cast<float>(gridGranularity);
                cgv.textCoords.y =
                        static_cast<float>(y)/
                        static_cast<float>(gridGranularity);;
                cgv.textCoords.z = 0.f;

                cgv.value = 0.0f;
                cgv.normal.x = 0.f;
                cgv.normal.y = 0.f;
                cgv.normal.z = 0.f;

                cgv.materialId = 0;
                vertices[index] = cgv;
            }
        }
    }

#pragma omp parallel for
    for(x=0; x<gridGranularity; ++x)
    {
        for(size_t y=0; y<gridGranularity; ++y)
        {
            for(size_t z=0; z<gridGranularity; ++z)
            {
                size_t index =
                        x*gridGranularity*gridGranularity +
                        y*gridGranularity + z;
                CubeGridCube cgc;
                cgc.vertices[0] = &vertices[
                        (x*(gridGranularity+1)+y)*
                        (gridGranularity+1)+z];
                cgc.vertices[1] = &vertices[
                        (x*(gridGranularity+1)+y)*
                        (gridGranularity+1)+z+1];
                cgc.vertices[2] = &vertices[
                        (x*(gridGranularity+1)+(y+1))*
                        (gridGranularity+1)+z+1];
                cgc.vertices[3] = &vertices[
                        (x*(gridGranularity+1)+(y+1))*
                        (gridGranularity+1)+z];
                cgc.vertices[4] = &vertices[
                        ((x+1)*(gridGranularity+1)+y)*
                        (gridGranularity+1)+z];
                cgc.vertices[5] = &vertices[
                        ((x+1)*(gridGranularity+1)+y)*
                        (gridGranularity+1)+z+1];
                cgc.vertices[6] = &vertices[
                        ((x+1)*(gridGranularity+1)+(y+1))*
                        (gridGranularity+1)+z+1];
                cgc.vertices[7] = &vertices[
                        ((x+1)*(gridGranularity+1)+(y+1))*
                        (gridGranularity+1)+z];
                cubes[index] = cgc;
            }
        }
    }

    BRAYNS_DEBUG << "Nb metaballs   : " <<
                    numMetaballs << std::endl;
    BRAYNS_DEBUG << "Nb Vertices    : " <<
                    numVertices << std::endl;
    BRAYNS_DEBUG << "Nb Cubes       : " <<
                    gridGranularity*gridGranularity*gridGranularity <<
                    std::endl;
    BRAYNS_DEBUG << "Grid size      : " <<
                    gridGranularity << std::endl;
    BRAYNS_DEBUG << "Grid dimensions: " <<
                    bounds << "/" << bounds.size() << std::endl;
    BRAYNS_DEBUG << "Grid center    : " <<
                    center << std::endl;

    vec3f ballToPoint;
    float squaredRadius;
    vec3f ballPosition;
    float normalScale;
    size_t materialId;
    const MetaballSphere* metaball;

#pragma omp parallel for private(metaball, squaredRadius, ballPosition, materialId)
    for ( size_t b=0; b<metaballs.size(); ++b )
    {
        metaball = &metaballs[b];
        if( metaball )
        {
            squaredRadius = metaball->sphere.r*metaball->sphere.r;
            ballPosition = metaball->sphere.v;
            materialId = metaball->materialId;

            size_t i=0;
#pragma omp parallel for private(ballToPoint, normalScale)
            for ( i=0; i<numVertices; ++i )
            {
                CubeGridVertex* vertex = &vertices[i];
                if( vertex )
                {
                    ballToPoint = vertex->position - ballPosition;

                    // get squared distance from ball to point
                    // float squaredDistance=ballToPoint.GetSquaredLength();
                    float distance = length(ballToPoint);

                    float squaredDistance =
                            distance*distance*distance*distance;

                    if(squaredDistance==0.0f)
                        squaredDistance=0.0001f;

                    normalScale =
                            squaredRadius/(squaredDistance*squaredDistance);

                    vertex->value +=
                            squaredRadius/squaredDistance;

                    if( squaredDistance < 0.001 * threshold)
                        vertex->materialId = materialId;

                    vertex->normal += ballToPoint * normalScale;
                }
            }
        }
    }

    BRAYNS_DEBUG << "Calculating which vertices are inside the surface" <<
                    std::endl;

    static SurfaceVertex edgeVertices[12];
    {
        //loop through cubes
        size_t i=0;
        for( i=0; i<numCubes; ++i)
        {
            const CubeGridCube& cube = cubes[i];
            unsigned char cubeIndex=0;

            if(cube.vertices[0]->value>0 && cube.vertices[0]->value<threshold)
                cubeIndex |= 1;
            if(cube.vertices[1]->value>0 && cube.vertices[1]->value<threshold)
                cubeIndex |= 2;
            if(cube.vertices[2]->value>0 && cube.vertices[2]->value<threshold)
                cubeIndex |= 4;
            if(cube.vertices[3]->value>0 && cube.vertices[3]->value<threshold)
                cubeIndex |= 8;
            if(cube.vertices[4]->value>0 && cube.vertices[4]->value<threshold)
                cubeIndex |= 16;
            if(cube.vertices[5]->value>0 && cube.vertices[5]->value<threshold)
                cubeIndex |= 32;
            if(cube.vertices[6]->value>0 && cube.vertices[6]->value<threshold)
                cubeIndex |= 64;
            if(cube.vertices[7]->value>0 && cube.vertices[7]->value<threshold)
                cubeIndex |= 128;

            /* look this value up in the edge table to see which edges to
             * interpolate along
             */
            int usedEdges = edgeTable[cubeIndex];

            /* if the cube is entirely within/outside surface, no faces */
            if(usedEdges==0 || usedEdges==255)
            {
                continue;
            }

            for(int currentEdge=0; currentEdge<12; ++currentEdge)
            {
                if(usedEdges & 1<<currentEdge)
                {
                    CubeGridVertex* v1=
                            cube.vertices[
                            verticesAtEndsOfEdges[currentEdge*2]];
                    CubeGridVertex* v2=
                            cube.vertices[
                            verticesAtEndsOfEdges[currentEdge*2+1]];

                    float delta=(threshold - v1->value)/(v2->value - v1->value);

                    edgeVertices[currentEdge].position.x=
                            v1->position.x+delta*(
                                v2->position.x-v1->position.x);
                    edgeVertices[currentEdge].position.y=
                            v1->position.y+delta*(
                                v2->position.y-v1->position.y);
                    edgeVertices[currentEdge].position.z=
                            v1->position.z+delta*(
                                v2->position.z-v1->position.z);

                    edgeVertices[currentEdge].normal.x=
                            v1->normal.x + delta*(v2->normal.x - v1->normal.x);
                    edgeVertices[currentEdge].normal.y=
                            v1->normal.y + delta*(v2->normal.y - v1->normal.y);
                    edgeVertices[currentEdge].normal.z=
                            v1->normal.z + delta*(v2->normal.z - v1->normal.z);

                    edgeVertices[currentEdge].materialId = v2->materialId;
                }
            }

            for(int k=0; triTable[cubeIndex][k]!=-1; k+=3)
            {
                size_t material =
                        edgeVertices[triTable[cubeIndex][k]].materialId;
                for( int f(0); f<3; ++f)
                {
                    vec3fa n,v;
                    n = edgeVertices[triTable[cubeIndex][k+f]].normal;
                    v = edgeVertices[triTable[cubeIndex][k+f]].position;

                    triangles[material].vertex.push_back(v);
                    triangles[material].normal.push_back(n);
                }
                size_t len = triangles[material].vertex.size()-3;
                triangles[material].index.push_back(vec3i(len,len+1,len+2));
            }
        }
    }

    delete cubes;
    delete vertices;
}

